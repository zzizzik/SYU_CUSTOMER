--식자재 테이블 생성
CREATE TABLE INGREDIENT(
	INGREDIENTCATEGORY VARCHAR2(15), 
	INGREDIENTCODE VARCHAR2(10), 
	INGREDIENTNAME VARCHAR2(20), 
	INGREDIENTCALORIE NUMBER(4)
   );

--식자재 테이블 기본키 생성
ALTER TABLE INGREDIENT ADD CONSTRAINT INGREDIENT_PK PRIMARY KEY (INGREDIENTCODE)
;

--식자재 시퀀스 생성
CREATE SEQUENCE INGREDIENT_SEQ MAXVALUE 9999 INCREMENT BY 1 START WITH 1 NOCYCLE
; 

--식자재 코드에 시퀀스 적용
INSERT INTO INGREDIENT(INGREDIENTCODE) VALUES(INGREDIENT_SEQ.NEXTVAL)
;